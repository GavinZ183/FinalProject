    public void populateChatRecord(){
        String chatRecord = "";
        for(String message: item.getMessages()){
            chatRecord = chatRecord + message;
        }
        txtChatRecord.setLineWrap(true);
        txtChatRecord.setWrapStyleWord(true);
        txtChatRecord.setText(chatRecord);
    }

